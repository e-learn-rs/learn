public class Animal 
{
	
	public Animal()
	{
		System.out.println("Is a animal");  
	} 
	
	public Animal(String name)
	{
		System.out.println(name +" is a name of an animal." );  
		
	}
	
	public void numberOfLegs(int number)
	{
		System.out.println("It has "+number+" of legs");  
	}

}
